#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if ((*s == '\0') && (c == '\0'))
		return ((char*)s);
	while (*s != '\0')
	{
		if (*s != c)
			s++;
		if (*s == c)
			return ((char*)s);
	}
	return (NULL);
}
